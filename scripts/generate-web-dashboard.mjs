#!/usr/bin/env node

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, "..");
const sourcePath = resolve(repoRoot, "dashboard-source/web-dashboard.json");
const outputPath = resolve(repoRoot, "src/app/dashboard/generated-dashboard.ts");
const verifyOnly = process.argv.includes("--verify");

const forbiddenPatterns = [
  /password/i,
  /passcode/i,
  /2fa/i,
  /two[- ]factor/i,
  /reset link/i,
  /ssn/i,
  /social security/i,
  /account number/i,
  /routing number/i,
  /bank/i,
  /ledger/i,
  /tax/i,
  /attorney/i,
  /medical/i,
  /therapy/i,
  /journal/i,
  /mail\/messages/i,
  /raw transcript/i,
  /\bclients?\b/i,
  /\bleads?\b/i,
  /linear issue/i,
  /private family/i,
  /\/Users\/cjwatts/i,
  /00 Master Command/i,
  /04 Wealth/i,
  /05 Vero Tech Care/i,
  /\bLinear\b/i,
  /\bCaptain\b/i,
  /\bXO\b/i,
  /\binbox\b/i,
  /\boutbox\b/i,
  /sourceRoot/i
];

function assert(condition, message) {
  if (!condition) {
    throw new Error(message);
  }
}

function assertString(value, path) {
  assert(typeof value === "string" && value.trim().length > 0, `${path} must be a non-empty string.`);
}

function walkStrings(value, path = "root", found = []) {
  if (typeof value === "string") {
    found.push([path, value]);
    return found;
  }
  if (Array.isArray(value)) {
    value.forEach((item, index) => walkStrings(item, `${path}[${index}]`, found));
    return found;
  }
  if (value && typeof value === "object") {
    Object.entries(value).forEach(([key, item]) => walkStrings(item, `${path}.${key}`, found));
  }
  return found;
}

function validateDashboardData(data) {
  assert(data && typeof data === "object" && !Array.isArray(data), "source must be an object.");
  assert(data.schemaVersion === 1, "schemaVersion must be 1.");
  assert(data.route === "/dashboard", "route must be /dashboard.");
  assertString(data.updated, "updated");
  assertString(data.summary, "summary");
  assert(data.status && typeof data.status === "object", "status must be an object.");
  ["label", "value", "detail"].forEach((field) => assertString(data.status[field], `status.${field}`));

  for (const collectionName of ["metrics", "sections", "accessPlan", "sourceContract"]) {
    assert(Array.isArray(data[collectionName]) && data[collectionName].length > 0, `${collectionName} must be a non-empty array.`);
  }

  data.metrics.forEach((metric, index) => {
    ["label", "value", "detail"].forEach((field) => assertString(metric[field], `metrics[${index}].${field}`));
  });

  data.sections.forEach((section, index) => {
    ["title", "kicker"].forEach((field) => assertString(section[field], `sections[${index}].${field}`));
    assert(Array.isArray(section.items) && section.items.length > 0, `sections[${index}].items must be non-empty.`);
    section.items.forEach((item, itemIndex) => assertString(item, `sections[${index}].items[${itemIndex}]`));
  });

  data.accessPlan.forEach((item, index) => assertString(item, `accessPlan[${index}]`));
  data.sourceContract.forEach((item, index) => assertString(item, `sourceContract[${index}]`));

  const violations = [];
  for (const [path, value] of walkStrings(data)) {
    for (const pattern of forbiddenPatterns) {
      if (pattern.test(value)) {
        violations.push(`${path}: matched ${pattern}`);
      }
    }
  }

  assert(
    violations.length === 0,
    `web dashboard source includes private-domain terms:\n${violations.join("\n")}`
  );
}

function renderDataModule(data) {
  const json = JSON.stringify(data, null, 2);
  return `// Generated by scripts/generate-web-dashboard.mjs. Do not edit manually.\n\nexport const dashboardData = ${json} as const;\n\nexport type DashboardData = typeof dashboardData;\n`;
}

const raw = readFileSync(sourcePath, "utf8");
const data = JSON.parse(raw);
validateDashboardData(data);

const rendered = renderDataModule(data);

if (verifyOnly) {
  const current = readFileSync(outputPath, "utf8");
  assert(current === rendered, "generated dashboard data is stale. Run npm run generate:dashboard.");
  console.log("Web dashboard source and generated data verified.");
} else {
  writeFileSync(outputPath, rendered, "utf8");
  console.log(`Generated ${outputPath}`);
}
